string = " Hello, World! "

print(string.upper())  # Output :   HELLO, WORLD!
print(string.lower())  # Output :   hello, world!
print(string.strip())  # Output :  Hello, World!  ตัดตัวสเปสบาร์
print(string.replace("H", "J"))  # Output : Jello, World!
print(string.split(","))  # Output :  [ 'Hello', 'World!' ]  result เป็น list
print(len(string))  # 15 นับสเปชชึ๊ด้วย


